#!/usr/bin/env python3
"""
Analyze Multi-LLM Experiment Results

Generates:
1. Main table: per-LLM F1, Delta vs SMOTE, win rate, p-value, Cohen's d
2. Refusal-adjusted analysis: excludes configs where any LLM had content filter issues
3. Friedman test: overall LLM effect
4. Inter-LLM correlation: Kendall's tau between improvement rankings
5. Content filter analysis: which LLMs refused which classes
6. LaTeX table: tab_multi_llm.tex
"""

import json
import numpy as np
from pathlib import Path
from scipy import stats
from itertools import combinations

RESULTS_DIR = Path(__file__).parent.parent / "results" / "multi_llm"
CACHE_DIR = Path(__file__).parent.parent / "cache" / "llm_generations"
TABLES_DIR = Path(__file__).parent.parent / "Escrito_Tesis" / "Tables"
TABLES_DIR.mkdir(parents=True, exist_ok=True)

# Minimum texts per class to consider a generation successful (not refused)
# <20 = clear content filter refusal; 20-50 = short but not a refusal
SEVERE_REFUSAL_THRESHOLD = 20


def load_results():
    results_file = RESULTS_DIR / "final_results.json"
    if not results_file.exists():
        results_file = RESULTS_DIR / "partial_results.json"
    if not results_file.exists():
        print("ERROR: No results found. Run exp_multi_llm.py first.")
        return []
    with open(results_file) as f:
        data = json.load(f)
    return data.get("results", [])


def analyze_content_refusals():
    """Analyze which LLMs refused to generate content for which classes.

    Only considers cache files that match the multi-LLM experiment format
    (have 'model' field matching directory name, indicating they were
    generated by exp_multi_llm.py, not old batch files from prior experiments).
    """
    MULTI_LLM_MODELS = {
        "gemini-3-flash-preview", "gpt-5-mini", "claude-haiku-4-5",
        "kimi-k2.5", "glm-5"
    }
    refusals = {}  # {model: [(dataset, class, n_texts), ...]}

    for model_dir in sorted(CACHE_DIR.iterdir()):
        if not model_dir.is_dir():
            continue
        model_name = model_dir.name
        if model_name not in MULTI_LLM_MODELS:
            continue

        # For each (dataset, class), find the file from exp_multi_llm
        # by checking that the stored 'model' field matches
        seen = {}  # (dataset, class) -> n_texts
        for f in model_dir.glob("*.json"):
            with open(f) as fh:
                data = json.load(fh)
            stored_model = data.get("model", "")
            # Only consider files generated by the multi-LLM experiment
            if stored_model != model_name:
                continue
            key = (data.get("dataset", "?"), data.get("class_name", "?"))
            n_texts = len(data.get("texts", []))
            # Keep the entry for this (dataset, class) combo
            # If multiple files exist, take the one from our experiment
            if key not in seen or n_texts > seen[key]:
                seen[key] = n_texts

        model_refusals = [
            (ds, cls, n) for (ds, cls), n in sorted(seen.items())
            if n < SEVERE_REFUSAL_THRESHOLD
        ]
        if model_refusals:
            refusals[model_name] = model_refusals

    return refusals


def get_refusal_affected_datasets(refusals):
    """Get set of datasets where ANY LLM had a content filter refusal."""
    affected = set()
    for model, items in refusals.items():
        for dataset, cls, n in items:
            affected.add(dataset)
    return affected


def compute_paired_stats(method_f1s, baseline_f1s, n_comparisons=5):
    """Paired t-test with Bonferroni correction."""
    method_f1s = np.array(method_f1s)
    baseline_f1s = np.array(baseline_f1s)
    n = len(method_f1s)
    deltas = method_f1s - baseline_f1s
    delta_mean = np.mean(deltas)
    delta_std = np.std(deltas, ddof=1) if n > 1 else 0.0

    if n > 1 and delta_std > 0:
        t_stat, p_val = stats.ttest_rel(method_f1s, baseline_f1s)
    else:
        t_stat, p_val = 0.0, 1.0

    cohen_d = delta_mean / (delta_std + 1e-10) if delta_std > 0 else 0.0
    bonf_p = min(p_val * n_comparisons, 1.0)
    win_rate = np.mean(deltas > 0)

    # Bootstrap CI
    rng = np.random.RandomState(42)
    boot = [np.mean(deltas[rng.choice(n, n, replace=True)]) for _ in range(5000)]
    ci = (np.percentile(boot, 2.5), np.percentile(boot, 97.5))

    return {
        "delta_mean": float(delta_mean),
        "delta_pp": float(delta_mean * 100),
        "ci_95": (float(ci[0] * 100), float(ci[1] * 100)),
        "p_value": float(p_val),
        "bonf_p": float(bonf_p),
        "cohen_d": float(cohen_d),
        "win_rate": float(win_rate),
        "n": n,
    }


def analyze_llms(results, llm_models, llm_display, smote_by_key, sw_by_llm,
                 label="ALL CONFIGS", exclude_datasets=None):
    """Run per-LLM analysis, optionally excluding certain datasets."""
    n_comparisons = len(llm_models)

    # Filter keys if excluding datasets
    if exclude_datasets:
        filtered_smote = {k: v for k, v in smote_by_key.items()
                          if k[0] not in exclude_datasets}
        filtered_sw = {m: {k: v for k, v in sw_by_llm[m].items()
                           if k[0] not in exclude_datasets}
                       for m in llm_models}
    else:
        filtered_smote = smote_by_key
        filtered_sw = sw_by_llm

    print(f"\n{'=' * 90}")
    print(f"PER-LLM RESULTS vs SMOTE ({label})")
    print(f"{'=' * 90}")
    print(f"\n{'LLM':<22} {'Mean F1':>8} {'D SMOTE':>10} {'95% CI':>20} {'p_Bonf':>8} {'d':>6} {'Win%':>6} {'n':>4}")
    print("-" * 85)

    all_stats = {}
    mean_f1s = {}
    for model in llm_models:
        common_keys = set(filtered_sw[model].keys()) & set(filtered_smote.keys())
        if not common_keys:
            continue

        sorted_keys = sorted(common_keys)
        sw_vals = [filtered_sw[model][k] for k in sorted_keys]
        sm_vals = [filtered_smote[k] for k in sorted_keys]

        st = compute_paired_stats(sw_vals, sm_vals, n_comparisons)
        all_stats[model] = st
        mean_f1 = np.mean(sw_vals)
        mean_f1s[model] = mean_f1
        disp = llm_display[model]
        sig = "*" if st["bonf_p"] < 0.05 else ""
        print(f"{disp:<22} {mean_f1:>8.4f} {st['delta_pp']:>+9.2f}p "
              f"[{st['ci_95'][0]:>+6.2f},{st['ci_95'][1]:>+6.2f}] "
              f"{st['bonf_p']:>7.4f}{sig} {st['cohen_d']:>6.2f} "
              f"{st['win_rate']*100:>5.1f}% {st['n']:>4d}")

    return all_stats, mean_f1s, filtered_sw, filtered_smote


def run_friedman_and_correlation(llm_models, llm_display, sw_by_llm, smote_by_key):
    """Run Friedman test and inter-LLM correlation."""
    common_all = set.intersection(*[set(sw_by_llm[m].keys()) for m in llm_models]) if llm_models else set()
    common_all = common_all & set(smote_by_key.keys())

    friedman_stat, friedman_p = None, None
    mean_tau = None

    # Friedman test
    print(f"\n{'=' * 90}")
    print("FRIEDMAN TEST: Effect of LLM choice")
    print(f"{'=' * 90}")

    if len(common_all) >= 3 and len(llm_models) >= 3:
        sorted_keys = sorted(common_all)
        groups = []
        for model in llm_models:
            groups.append([sw_by_llm[model][k] for k in sorted_keys])

        friedman_stat, friedman_p = stats.friedmanchisquare(*groups)
        print(f"  N configs: {len(sorted_keys)}")
        print(f"  Chi2 = {friedman_stat:.3f}, p = {friedman_p:.6f}")
        if friedman_p < 0.05:
            print("  -> SIGNIFICANT: LLM choice affects results")
        else:
            print("  -> NOT significant: LLM choice does NOT significantly affect results")
            print("     (Supports LLM-agnostic claim)")
    else:
        print("  Insufficient data for Friedman test")

    # Inter-LLM correlation
    print(f"\n{'=' * 90}")
    print("INTER-LLM CORRELATION (Kendall's tau on delta vs SMOTE)")
    print(f"{'=' * 90}")

    if len(llm_models) >= 2 and common_all:
        sorted_keys = sorted(common_all)
        deltas_by_llm = {}
        for model in llm_models:
            deltas_by_llm[model] = [sw_by_llm[model][k] - smote_by_key[k] for k in sorted_keys]

        taus = []
        for m1, m2 in combinations(llm_models, 2):
            tau, p = stats.kendalltau(deltas_by_llm[m1], deltas_by_llm[m2])
            taus.append(tau)
            d1, d2 = llm_display[m1], llm_display[m2]
            print(f"  {d1} vs {d2}: tau={tau:.3f}, p={p:.4f}")

        mean_tau = np.mean(taus)
        print(f"\n  Mean Kendall's tau: {mean_tau:.3f}")
        if mean_tau > 0.5:
            print("  -> Strong inter-LLM agreement on which datasets benefit most")
        elif mean_tau > 0.3:
            print("  -> Moderate inter-LLM agreement")
        else:
            print("  -> Weak inter-LLM agreement")
    else:
        print("  Insufficient data for correlation analysis")

    return friedman_stat, friedman_p, mean_tau


def main():
    results = load_results()
    if not results:
        return

    # ========================================================================
    # CONTENT FILTER ANALYSIS
    # ========================================================================
    print(f"{'=' * 90}")
    print("CONTENT FILTER REFUSAL ANALYSIS")
    print(f"{'=' * 90}")

    refusals = analyze_content_refusals()
    affected_datasets = get_refusal_affected_datasets(refusals)

    for model, items in sorted(refusals.items()):
        # Only count items from the 7 base datasets used in this experiment
        experiment_items = [i for i in items if any(
            i[0].startswith(ds) for ds in [
                "20newsgroups_", "sms_spam_", "hate_speech_davidson_",
                "ag_news_", "dbpedia14_", "emotion_", "20newsgroups_20class_"
            ]
        )]
        if experiment_items:
            print(f"\n  {model}: {len(experiment_items)} classes with <{SEVERE_REFUSAL_THRESHOLD} texts")
            for ds, cls, n in experiment_items:
                print(f"    {ds}/{cls}: {n} texts")

    print(f"\n  Affected datasets: {sorted(affected_datasets & set(r['dataset'] for r in results))}")

    # ========================================================================
    # SETUP
    # ========================================================================
    llm_models = sorted(set(r["llm_model"] for r in results if r["augmentation_method"] == "soft_weighted"))
    llm_display = {}
    for r in results:
        if r["llm_model"] not in llm_display:
            llm_display[r["llm_model"]] = r.get("llm_display", r["llm_model"])

    print(f"\nFound {len(llm_models)} LLMs: {[llm_display[m] for m in llm_models]}")

    smote_by_key = {}
    for r in results:
        if r["augmentation_method"] == "smote":
            key = (r["dataset"], r["classifier"])
            smote_by_key[key] = r["f1_macro"]

    sw_by_llm = {m: {} for m in llm_models}
    for r in results:
        if r["augmentation_method"] == "soft_weighted":
            key = (r["dataset"], r["classifier"])
            sw_by_llm[r["llm_model"]][key] = r["f1_macro"]

    # ========================================================================
    # RAW RESULTS (ALL CONFIGS)
    # ========================================================================
    raw_stats, raw_f1s, _, _ = analyze_llms(
        results, llm_models, llm_display, smote_by_key, sw_by_llm,
        label="ALL CONFIGS — RAW"
    )

    # ========================================================================
    # REFUSAL-ADJUSTED RESULTS
    # ========================================================================
    # Exclude datasets where ANY LLM had refusals
    experiment_datasets = set(r["dataset"] for r in results)
    affected_in_experiment = affected_datasets & experiment_datasets

    if affected_in_experiment:
        adj_stats, adj_f1s, adj_sw, adj_smote = analyze_llms(
            results, llm_models, llm_display, smote_by_key, sw_by_llm,
            label="REFUSAL-ADJUSTED (excl. datasets with any refusal)",
            exclude_datasets=affected_in_experiment
        )
    else:
        adj_stats = raw_stats
        adj_f1s = raw_f1s
        adj_sw = sw_by_llm
        adj_smote = smote_by_key

    # ========================================================================
    # FRIEDMAN + CORRELATION (on adjusted data for fair comparison)
    # ========================================================================
    friedman_stat, friedman_p, mean_tau = run_friedman_and_correlation(
        llm_models, llm_display, adj_sw, adj_smote
    )

    # ========================================================================
    # PER N-SHOT BREAKDOWN
    # ========================================================================
    print(f"\n{'=' * 90}")
    print("PER N-SHOT BREAKDOWN (all LLMs combined, adjusted)")
    print(f"{'=' * 90}")

    for nshot in [10, 25, 50]:
        all_sw, all_sm = [], []
        for model in llm_models:
            for key in sorted(set(adj_sw.get(model, sw_by_llm[model]).keys()) & set(adj_smote.keys())):
                ds, clf = key
                if f"_{nshot}shot" in ds:
                    all_sw.append(adj_sw.get(model, sw_by_llm[model])[key])
                    all_sm.append(adj_smote[key])
        if all_sw:
            deltas = np.array(all_sw) - np.array(all_sm)
            print(f"  {nshot}-shot: D={np.mean(deltas)*100:+.2f}pp, "
                  f"Win={np.mean(deltas>0)*100:.1f}%, n={len(deltas)}")

    # ========================================================================
    # CONSISTENCY CHECK: Range of Deltas across LLMs
    # ========================================================================
    print(f"\n{'=' * 90}")
    print("CROSS-LLM CONSISTENCY")
    print(f"{'=' * 90}")

    deltas_all = [adj_stats[m]["delta_pp"] for m in llm_models if m in adj_stats]
    if deltas_all:
        print(f"  Range of D vs SMOTE: {min(deltas_all):+.2f}pp to {max(deltas_all):+.2f}pp")
        print(f"  Spread: {max(deltas_all) - min(deltas_all):.2f}pp")
        print(f"  Mean D: {np.mean(deltas_all):+.2f}pp")
        print(f"  All positive: {'YES' if all(d > 0 for d in deltas_all) else 'NO'}")
        n_sig = sum(1 for m in llm_models if m in adj_stats and adj_stats[m]["bonf_p"] < 0.05)
        print(f"  Significant (Bonf.): {n_sig}/{len(deltas_all)}")

    # ========================================================================
    # GENERATE LATEX TABLE (use adjusted stats)
    # ========================================================================
    print(f"\n{'=' * 90}")
    print("GENERATING LATEX TABLE")
    print(f"{'=' * 90}")

    use_stats = adj_stats
    use_f1s = adj_f1s
    use_sw = adj_sw
    use_smote = adj_smote
    n_comparisons = len(llm_models)
    n_configs = len(set(adj_smote.keys()))

    # Determine how many datasets remain after filtering
    adj_dataset_names = set(k[0] for k in adj_smote.keys())

    latex_lines = [
        r"\begin{table}[htbp]",
        r"\centering",
        r"\small",
        r"\caption{Rendimiento del filtrado geom\'etrico con ponderaci\'on suave (cascade L1) usando 5 LLMs distintos. "
        r"$\Delta$ vs SMOTE y tasa de victoria promediados sobre " + str(len(adj_dataset_names)) +
        r" configuraciones $\times$ 3 clasificadores lineales. "
        r"Se excluyen datasets donde alg\'un LLM rechaz\'o la generaci\'on por filtros de contenido. "
        r"$p$ corregido con Bonferroni ($k=" + str(n_comparisons) + r"$).}",
        r"\label{tab:multi_llm}",
        r"\begin{tabular}{llcccccc}",
        r"\toprule",
        r"\textbf{LLM} & \textbf{Tipo} & \textbf{F1 Macro} & \textbf{$\Delta$ SMOTE} & "
        r"\textbf{IC 95\%} & \textbf{$p_{\text{Bonf}}$} & \textbf{$d$} & \textbf{Victoria} \\",
        r"\midrule",
    ]

    type_map = {
        "gemini-3-flash-preview": "Closed",
        "gpt-5-mini": "Closed",
        "claude-haiku-4-5": "Closed",
        "kimi-k2.5": "OSS",
        "glm-5": "OSS",
    }

    # Find best delta for bold highlighting
    best_delta = max((use_stats[m]["delta_pp"] for m in llm_models if m in use_stats), default=0)

    for model in llm_models:
        if model not in use_stats:
            continue
        st = use_stats[model]
        disp = llm_display[model]
        mtype = type_map.get(model, "?")
        mean_f1 = use_f1s.get(model, 0) * 100
        sig = r"$^{*}$" if st["bonf_p"] < 0.05 else ""
        ci_str = f"[{st['ci_95'][0]:+.1f}, {st['ci_95'][1]:+.1f}]"
        p_str = f"{st['bonf_p']:.3f}" if st["bonf_p"] >= 0.001 else "$<$0.001"

        # Bold the best row
        delta_str = f"{st['delta_pp']:+.2f}"
        if abs(st['delta_pp'] - best_delta) < 0.01:
            disp = r"\textbf{" + disp + "}"
            delta_str = r"\textbf{" + delta_str + "}"

        latex_lines.append(
            f"{disp} & {mtype} & {mean_f1:.1f} & {delta_str}{sig} & "
            f"{ci_str} & {p_str} & {st['cohen_d']:.2f} & {st['win_rate']*100:.1f}\\% \\\\"
        )

    latex_lines.extend([
        r"\bottomrule",
        r"\end{tabular}",
    ])

    # Add Friedman test footnote
    footnote_parts = []
    if friedman_p is not None:
        p_label = f"{friedman_p:.3f}" if friedman_p >= 0.001 else "<0.001"
        sig_label = "no significativo" if friedman_p >= 0.05 else "significativo"
        footnote_parts.append(
            r"Test de Friedman: $\chi^2 = " + f"{friedman_stat:.2f}" +
            r"$, $p = " + p_label + r"$ (" + sig_label + r")."
        )
    if mean_tau is not None:
        footnote_parts.append(
            r"Correlaci\'on inter-LLM media: $\tau_K = " + f"{mean_tau:.2f}" + r"$."
        )

    # Note about excluded datasets
    if affected_in_experiment:
        n_excluded = len(affected_in_experiment)
        footnote_parts.append(
            f"{n_excluded} datasets excluidos por rechazo de contenido en $\\geq$1 LLM."
        )

    if footnote_parts:
        latex_lines.append(r"\par\smallskip")
        latex_lines.append(r"{\small " + " ".join(footnote_parts) + "}")

    latex_lines.append(r"\end{table}")

    latex_content = "\n".join(latex_lines)
    latex_path = TABLES_DIR / "tab_multi_llm.tex"
    with open(latex_path, "w") as f:
        f.write(latex_content)
    print(f"  Saved to {latex_path}")
    print(f"\n{latex_content}")

    # ========================================================================
    # ALSO GENERATE RAW TABLE (for appendix/discussion)
    # ========================================================================
    print(f"\n{'=' * 90}")
    print("RAW TABLE (including refusal-affected configs)")
    print(f"{'=' * 90}")

    raw_latex = [
        r"\begin{table}[htbp]",
        r"\centering",
        r"\small",
        r"\caption{Resultados sin ajustar por rechazo de contenido. "
        r"Incluye todas las 21 configuraciones. "
        r"Los LLMs con filtros de seguridad m\'as agresivos (Kimi K2.5, Claude 4.5 Haiku) "
        r"muestran menor $\Delta$ debido a generaciones rechazadas en clases sensibles "
        r"(discurso de odio, contenido religioso, spam).}",
        r"\label{tab:multi_llm_raw}",
        r"\begin{tabular}{llccccc}",
        r"\toprule",
        r"\textbf{LLM} & \textbf{Tipo} & \textbf{$\Delta$ SMOTE} & "
        r"\textbf{IC 95\%} & \textbf{$d$} & \textbf{Victoria} & \textbf{Rechazos} \\",
        r"\midrule",
    ]

    for model in llm_models:
        if model not in raw_stats:
            continue
        st = raw_stats[model]
        disp = llm_display[model]
        mtype = type_map.get(model, "?")
        sig = r"$^{*}$" if st["bonf_p"] < 0.05 else ""
        ci_str = f"[{st['ci_95'][0]:+.1f}, {st['ci_95'][1]:+.1f}]"
        n_refusals = len(refusals.get(model, []))

        raw_latex.append(
            f"{disp} & {mtype} & {st['delta_pp']:+.2f}{sig} & "
            f"{ci_str} & {st['cohen_d']:.2f} & {st['win_rate']*100:.1f}\\% & {n_refusals} \\\\"
        )

    raw_latex.extend([
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{table}",
    ])

    raw_content = "\n".join(raw_latex)
    raw_path = TABLES_DIR / "tab_multi_llm_raw.tex"
    with open(raw_path, "w") as f:
        f.write(raw_content)
    print(f"  Saved to {raw_path}")
    print(f"\n{raw_content}")

    # ========================================================================
    # SAVE FULL ANALYSIS
    # ========================================================================
    analysis = {
        "raw_results": {llm_display[m]: raw_stats[m] for m in llm_models if m in raw_stats},
        "adjusted_results": {llm_display[m]: adj_stats[m] for m in llm_models if m in adj_stats},
        "friedman_p": friedman_p,
        "friedman_stat": friedman_stat,
        "mean_kendall_tau": mean_tau,
        "n_llms": len(llm_models),
        "n_datasets_total": len(set(r["dataset"] for r in results)),
        "n_datasets_adjusted": len(adj_dataset_names),
        "excluded_datasets": sorted(affected_in_experiment) if affected_in_experiment else [],
        "content_refusals": {
            model: [(ds, cls, n) for ds, cls, n in items]
            for model, items in refusals.items()
        },
        "timestamp": __import__("datetime").datetime.now().isoformat(),
    }
    with open(RESULTS_DIR / "analysis.json", "w") as f:
        json.dump(analysis, f, indent=2)
    print(f"\n  Analysis saved to {RESULTS_DIR / 'analysis.json'}")


if __name__ == "__main__":
    main()
